<p class="ftco-animate">Even so, I had always been fascinated by the Ashtanga Yoga method. I was familiar with the basics, which my brother Yogesh had taught me in earlier years. Upon my return to Delhi,<span style="color:#CC0000;"> I began a committed period of learning from Ajay Tokas, a dedicated teacher and a family friend.</span> From him, I slowly and patiently deepened my understanding of this traditional and powerful practice. Over time, I saw positive changes to my emotional and physical health  weight loss, increased bodily strength and flexibility, improved breathing, better sleep and reduced anxiety.</p>


<p class="ftco-animate">In January 2019, I became authorized to teach the <span style="color:#CC0000;">Ashtanga Yoga method by the Krishna Pattabhi Jois Ashtanga Yoga Institute (KPJAYI), in Mysore, south India.</span> </p>

