<p class="ftco-animate">I was surprised by this moment of honest self realization, because I had no substantial training in yoga. As is common in most conventional Indian families, I was expected to study engineering or finance. In 2007, after graduating from the University of Delhi, I began working at an entry-level position with a multinational company. With this job, I wanted to not only save for my further education but also contribute to my family's income.</p>    

<p class="ftco-animate">At first, the fast-paced corporate culture was exciting. I was appreciated for my skills, learnt well, and made steady progress for my efforts. My colleagues became friends, and I enjoyed socializing after company hours. I loved having a high disposable income and fancy perks. But that was not all. I worked very hard to achieve, and along the way, also completed my MBA degree.</p>

<p class="ftco-animate">I cannot say when things started to change for me. I was just in my mid twenties, but I had begun to feel like a rusty, middle-aged wheel stuck in a muddy rut. I sensed the need for a deeper goal, but I could not see it clearly yet. Meanwhile, my health was suffering. The high levels of stress in daily life had given me careless habits, and I made no time to exercise, eat sensibly or rest adequately.<span style="color:#CC0000;"> By the summer of 2014, I was shocked that my weight had shot up to 86 kgs.</span> </p>

<p class="ftco-animate">I knew then that what I wanted most, more than the next cash bonus or career milestone, was to be physically fit and emotionally alive.<span style="color:#CC0000;"> In early 2015, I found myself at the Sivananda Yoga Vedanta Dhanwantari Ashram in Kerala, south India.</span> The atmosphere of discipline and devotion that I absorbed there gave me peace and time to introspect.</p>
