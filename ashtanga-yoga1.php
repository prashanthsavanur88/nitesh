			  <h3 >B.	TRADITION AND PRACTICE : Krishna PattabhiJois Ashtanga Yoga Institute (KPJAYI) – Mysore, India</h3>
	          <p class="ftco-animate" >In the late 1920s, the eminent yoga and Sanskrit scholar T. Krishnamacharya discovered a copy of the Yoga Korunta in the Calcutta University Library. This ancient text, compiled by sage Vamana, consisted of about 100 stanzas written on palm leaves. But it had been virtually destroyed by ants. Krishnam acharya, together with his young disciple KrishnaPattabhiJois, set about transcribing what remained of the priceless manuscript. This includeda detailed explanation of asanas(postures) with their individual benefits, the order or sequences in which they were to be performed, and movements that indicated how to transition from one posture to the next.  </p>
			  <p>These are the vital components of the Ashtanga Yoga method as devised by K. PattabhiJois; as is traditionally taught at the KPJAYI in Mysore, south India and by authorized teachers around the world :</p>
			  
			  <div class="col-md-12 " style="text-align:center; padding:10px;">
