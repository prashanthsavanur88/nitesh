			  <p class="ftco-animate" >Vinyasa : The system that links the breath to the flowing movement between postures. For each movement, there is one breath taken. Practicing vinyasa‘boils’, purifies and thins the blood. The heated blood then circulates freely around the joints, removing bodily pain. It also moves easily through the internal organs of the body, removing impurities and disease which are excreted through the sweat that the practice generates. The heat, or internal fire of vinyasa, is also known to increase human metabolism and lifespan. </p>
				 <p class="ftco-animate" >Tristhana: The three points of attention or action – posture, breath and drishthi, or fixed point of gaze. When these elements are combined, purification takes place in the body, the nervous system and the mind. Yoga posturescleanse, strengthen and give flexibility to the body. Steady, regulated breathing purifies the nervous system. Focused application of drishthi, of which there are nine types in total, stabilizes the mind and all mental processes.</p>
				 <p class="ftco-animate" >Bandhas : Two bandhas, or energy locks, are considered critical to the method : mulabandha (root lock) and uddiyanbandha (abdominal lock). These seal in and direct the flow of prana (vital energy) in the body, bringing about a feeling of lightness, strength and control. </p>
				 <p class="ftco-animate" >The Six Poisons: In the Yoga Shastras, it is said that God dwells in the human spiritual heart in the form of light, but that this light is shadowed by six poisons :kama (desire), krodha (anger), moha (delusion), lobha (greed), matsarya(envy), and mada (sloth). When dedicated yoga practice is sustained over time, the heat generated from it burns away these poisons, and the pure light of one’s inner nature shines forth.</p>
				 <p class="ftco-animate" >Paramparaand Traditional Learning :A Sanskrit word, parampara denotes the transmission of traditional knowledge from teacher to student. It is the basis of any lineage; where the guru (teacher) and shishya(disciple) are the links in a chain of instruction that has existed for thousands of years, and where knowledge is passed down through direct instruction as well as practical experience. </p>
				 <p class="ftco-animate" >In ancient India, the guru-shishya relationship was one of sanctity and mutual commitment. The role of the teacher was to transfer knowledge in its purest form, with the noblest intention and purpose. The dharma, or duty of the student was to respect and obey the guru, and to learn by diligence, effort and the surrender of personal ego.  </p>
